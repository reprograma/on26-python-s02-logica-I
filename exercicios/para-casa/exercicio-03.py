# Quetão 03 - Faça um programa, com uma função que necessite de três argumentos, e que forneça a soma desses três argumentos.

# Somar argumentos simples
numero1 = 15
numero2 = 5
numero3 = 30

def calcular_soma(numero1, numero2, numero3): 
    return numero1 + numero2 + numero3 

resultado = calcular_soma(15, 5 , 30)

print("valor é:", resultado)
